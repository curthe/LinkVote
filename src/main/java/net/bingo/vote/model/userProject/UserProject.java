package net.bingo.vote.model.userProject;

public class UserProject {
	String id;// varchar(32) comment '用户工程表id',
	String uId;// varchar(32) comment '用户id',
	String pId;// varchar(32) comment '工程id',
	String participation;// int comment '用户参与情况',
	String isCreateItem;// int comment '是否可以新增项目',

	@Override
	public String toString() {
		return "UserProject [id=" + id + ", uId=" + uId + ", pId=" + pId + ", participation=" + participation
				+ ", isCreateItem=" + isCreateItem + "]";
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getuId() {
		return uId;
	}

	public void setuId(String uId) {
		this.uId = uId;
	}

	public String getpId() {
		return pId;
	}

	public void setpId(String pId) {
		this.pId = pId;
	}

	public String getParticipation() {
		return participation;
	}

	public void setParticipation(String participation) {
		this.participation = participation;
	}

	public String getIsCreateItem() {
		return isCreateItem;
	}

	public void setIsCreateItem(String isCreateItem) {
		this.isCreateItem = isCreateItem;
	}

}
